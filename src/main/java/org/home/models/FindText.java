package org.home.models;

public class FindText {
	
	private TranslatesT tt = new TranslatesT();
	private SubjectT st = new SubjectT();
	private BindSubj bs = new BindSubj();
	
	public TranslatesT getTt() {
		return tt;
	}
	public void setTt(TranslatesT tt) {
		this.tt = tt;
	}
	public SubjectT getSt() {
		return st;
	}
	public void setSt(SubjectT st) {
		this.st = st;
	}
	public BindSubj getBs() {
		return bs;
	}
	public void setBs(BindSubj bs) {
		this.bs = bs;
	}
	

}
